#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn every_language_has_every_message() {
        for language in [
            AppLanguage::Portuguese,
            AppLanguage::English,
            AppLanguage::Spanish,
        ] {
            for message in ALL_MESSAGES {
                assert!(
                    !text(language, *message).trim().is_empty(),
                    "missing translation for {language:?} / {message:?}"
                );
            }
        }
    }
}
